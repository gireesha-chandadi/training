package Debugging;

public class Debugging {
    public static void main(String[] args) {
        // this is a debugging program
        int i = 10;
        int j = 30;
        String s = "java";
        double l = 3.14;
        System.out.println(i + j);
        System.out.println(i - j);
        System.out.println(i + l);
        System.out.println(i - l);
        System.out.println(s);

    }

}